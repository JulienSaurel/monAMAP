<h1> Nos contrats </h1>

<h3> Le système de contrats de l'AMAP d'O </h3>
<p> L'AMAP d'O repose sur un système de contrats entre les producteurs et les consommateurs. Ces derniers peuvent souscrire à des contrats 
	leur permettant d'avoir droit, à la fréquence choisie, à un panier au choix rempli de produits BIOs directement issus des fermes de nos producteurs.

	L'adhérent peut choisir la taille de son panier (S/M/L), le type (végétal etc...), ainsi que la fréquence de distribution (hebdomadaire etc...).
</p>

