		<h2>Joseph ROUX</h2>
		<h5>Eleveur de bovins à Saint-Jean-de-Buèges</h5>
		<p>Les animaux de Joseph vivent librement sur 300ha2 tout au long de l'année.</p>
		<p>Les viandes de boeuf et veau proposés à l'AMAP d'O par Joseph sont issues de l'agriculture Biologique et responsable</p>
	</div>

	<div>
