		<h2>La famille BANCHERES</h2>
		<h5>Maraîcher à Cournonterral.</h5>
		<p>Depuis 1768, la famille BANCHERES produit elle même ses fruits et légumes en agriculture biologique dans un espace de 4 hectares. La famille applique les principes de la biodynamie.</p>
		<p>Les BANCHERES offrent leur produits depuis la création de l'AMAP d'O.</p>
	</div>
		
	<div>
		<img src="./images/Producteur2.png" alt="Producteur 2"/>
		<h2>Estelle PETIT</h2>
		<h5>Maraîchère à St Georges d'Orques.</h5>
		<p>Estelle s'est installée il y a 10ans dans la région Occitanie, à St Georges d'Orques. Depuis, elle cultive avec amour et attention ses tomates en agriculture biologique. </p>
		<p>Estelle est une adhérente à l'AMAP d'O qui s'est proposée à partager ses fruits en 2015.</p>
	</div>

	<div>
