		<h2>Bérangère FOURNIER</h2>
		<h5>Eleveuse de poule à Poujol-sur-Orb</h5>
		<p>Bérangère élèves ses poules, poulets et chapons en plein air et en appliquant les principes de l'agriculture biologique depuis maintenant 6ans.</p>
		<p>Elle fournit à l'AMAP d'O volailles et oeufs</p>
	</div>

