		<h5>Eleveur et producteur de fromages à Sorèze.</h5>
		<p>Etienne élève ses vaches laîtières et ses chèvres en accord avec les valeurs de l'agriculture biologique et responsable, dans le respect des animaux et de l'environnement.</p>
		<p>Il prosope des fromages et laits crus d'exception en quantité rare pour le respect de ses animaux.</p>
